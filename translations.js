// ================= LANGUAGE SWITCHER LOGIC =================

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
  const savedLanguage = localStorage.getItem('selectedLanguage') || 'id';
  setLanguage(savedLanguage);
});

// Set language and update page
function setLanguage(lang) {
  if (!translations[lang]) lang = 'id';
  
  localStorage.setItem('selectedLanguage', lang);
  
  // Update HTML lang attribute
  document.documentElement.lang = lang;
  document.querySelector('html').lang = lang;
  
  // Update language switcher button
  const langBtns = document.querySelectorAll('.lang-btn');
  langBtns.forEach(btn => {
    btn.classList.remove('active');
    if (btn.getAttribute('data-lang') === lang) {
      btn.classList.add('active');
    }
  });
  
  // Update page title
  const currentPage = getCurrentPage();
  updatePageTitle(currentPage, lang);
  
  // Update all translatable elements
  updatePageTranslations(lang);
}

// Get current page based on URL
function getCurrentPage() {
  const path = window.location.pathname;
  if (path.includes('map.html')) return 'map';
  if (path.includes('tematrip.html')) return 'tematrip';
  if (path.includes('tourismmaps.html')) return 'tourismmaps';
  if (path.includes('profile.html')) return 'profile';
  return 'home';
}

// Update page title
function updatePageTitle(page, lang) {
  const titles = {
    en: { home: 'Home - Telusur Kota', map: 'Story Maps - Telusur Kota', tematrip: 'Theme Trips - Telusur Kota', tourismmaps: 'Tourism Maps - Telusur Kota', profile: 'About Us - Telusur Kota' },
    id: { home: 'Beranda - Telusur Kota', map: 'Story Maps - Telusur Kota', tematrip: 'Tema Trip - Telusur Kota', tourismmaps: 'Tourism Maps - Telusur Kota', profile: 'Tentang Kami - Telusur Kota' }
  };
  document.title = titles[lang][page] || titles[lang]['home'];
}

// Update all translatable elements on page
function updatePageTranslations(lang) {
  // Update navigation
  const navLinks = {
    'nav-beranda': translations[lang].nav.beranda,
    'nav-tematrip': translations[lang].nav.temaTrip,
    'nav-storymap': translations[lang].nav.storyMaps,
    'nav-tourismmaps': translations[lang].nav.tourismMaps,
    'nav-tentangkami': translations[lang].nav.tentangKami
  };
  
  Object.entries(navLinks).forEach(([id, text]) => {
    const el = document.getElementById(id);
    if (el) el.textContent = text;
  });
  
  // Update hero section
  const heroTitle = document.querySelector('.hero-content h1');
  if (heroTitle) heroTitle.innerHTML = translations[lang].hero.title;
  
  const heroSubtitle = document.querySelector('.hero-content p');
  if (heroSubtitle) heroSubtitle.textContent = translations[lang].hero.subtitle;
  
  // Update about section
  const aboutTitle = document.getElementById('about-title');
  if (aboutTitle) aboutTitle.textContent = translations[lang].about.title;
  
  const aboutDesc = document.getElementById('about-description');
  if (aboutDesc) aboutDesc.textContent = translations[lang].about.description;
  
  const btnStoryMaps = document.getElementById('btn-storyMaps');
  if (btnStoryMaps) btnStoryMaps.textContent = translations[lang].about.storyMaps;
  
  const btnTourismMaps = document.getElementById('btn-tourismMaps');
  if (btnTourismMaps) btnTourismMaps.textContent = translations[lang].about.tourismMaps;
  
  // Update feature cards
  updateFeatureCards(lang);
  
  // Update footer
  const footerContactHeading = document.getElementById('footer-contact-heading');
  if (footerContactHeading) footerContactHeading.textContent = translations[lang].footerContact.heading;
  
  const footerLocation = document.getElementById('footer-location');
  if (footerLocation) footerLocation.textContent = translations[lang].footerContact.location;
  
  const footerEmail = document.getElementById('footer-email');
  if (footerEmail) footerEmail.textContent = translations[lang].footerContact.email;
  
  const footerPhone = document.getElementById('footer-phone');
  if (footerPhone) footerPhone.textContent = translations[lang].footerContact.phone;
  
  const footerCopyright = document.getElementById('footer-copyright');
  if (footerCopyright) footerCopyright.textContent = translations[lang].footerContact.copyright;
  
  // Trigger custom event for other scripts to handle
  window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
}

